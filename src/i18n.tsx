const DICTS: Record<Lang, Dict> = { zh, en, vi, id, th }

// ── Context ─────────────────────────────────────────────
interface I18nCtx {
  lang: Lang
  setLang: (l: Lang) => void
  t: (key: string, vars?: Record<string, string | number>) => string
  /** 相對時間（多語） */
  rt: (iso: string) => string
}

const I18nContext = createContext<I18nCtx | null>(null)

const STORAGE_KEY = 'guardian_lang'

function detectLang(): Lang {
  const saved = localStorage.getItem(STORAGE_KEY) as Lang | null
  if (saved && DICTS[saved]) return saved
  const nav = navigator.language.toLowerCase()
  if (nav.startsWith('vi')) return 'vi'
  if (nav.startsWith('id') || nav.startsWith('in')) return 'id'
  if (nav.startsWith('th')) return 'th'
  if (nav.startsWith('en')) return 'en'
  return 'zh'
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(detectLang)

  const setLang = useCallback((l: Lang) => {
    setLangState(l)
    localStorage.setItem(STORAGE_KEY, l)
    document.documentElement.lang = l === 'zh' ? 'zh-TW' : l
  }, [])

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      let str = DICTS[lang][key] ?? DICTS.zh[key] ?? key
      if (vars) {
        for (const [k, v] of Object.entries(vars)) {
          str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v))
        }
      }
      return str
    },
    [lang],
  )

  const rt = useCallback(
    (iso: string) => {
      const diff = Math.floor((Date.now() - new Date(iso).getTime()) / 60000)
      if (diff < 1) return t('common.justNow')
      if (diff < 60) return t('common.minsAgo', { n: diff })
      return t('common.hoursAgo', { n: Math.floor(diff / 60) })
    },
    [t],
  )

  return (
    <I18nContext.Provider value={{ lang, setLang, t, rt }}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useI18n must be inside I18nProvider')
  return ctx
}
